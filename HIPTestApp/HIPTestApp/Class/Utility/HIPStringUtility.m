+ (NSString *)firstLetterUsingSeperate:(NSString *)seperate chineseString:(NSString *)str includeNumber:(BOOL)includeNumber {
    __block NSString *firstLetters = @"";
    [str enumerateCharaterUsingBlock:^(unsigned short letter, BOOL *stop) {
        
        int index = letter - HIP_HANZI_START;
        
        if (index >= 0 && index <= HIP_HANZI_START){
            if ([firstLetters length]) {
                firstLetters = [firstLetters stringByAppendingFormat:@"%@%c", seperate, HIPFirstLetterArray[index]];
            } else {
                firstLetters = [firstLetters stringByAppendingFormat:@"%c", HIPFirstLetterArray[index]];
            }
        } else if ((letter >= 'a' && letter <= 'z') || (letter >= 'A' && letter <= 'Z')) {
            if ([firstLetters length]){
                firstLetters = [firstLetters stringByAppendingFormat:@"%@%c", seperate, letter];
            } else {
                firstLetters = [firstLetters stringByAppendingFormat:@"%c", letter];
            }
        } else if (includeNumber && (letter >= '0' && letter <= '9')) {
            if ([firstLetters length]){
                firstLetters = [firstLetters stringByAppendingFormat:@"%@%c", seperate, letter];
            } else {
                firstLetters = [firstLetters stringByAppendingFormat:@"%c", letter];
            }
        } else {
            //如果是字母或其它符号，都返回 #
            if ([firstLetters length]) {
                firstLetters = [firstLetters stringByAppendingFormat:@"%@%c", seperate, '#'];
            } else {
                firstLetters = [firstLetters stringByAppendingFormat:@"%c", '#'];
            }
        }
    }];
    return [firstLetters lowercaseString];
}

+ (NSData *)convertHexStrToData:(NSString *)str {
    if (!str || [str length] == 0) {
        return nil;
    }
    
    NSMutableData *hexData = [[NSMutableData alloc] initWithCapacity:8];
    NSRange range;
    if ([str length] % 2 == 0) {
        range = NSMakeRange(0, 2);
    } else {
        range = NSMakeRange(0, 1);
    }
    
    for (NSInteger i = range.location; i < [str length]; i += 2) {
        unsigned int anInt;
        NSString *hexCharStr = [str substringWithRange:range];
        NSScanner *scanner = [[NSScanner alloc] initWithString:hexCharStr];
        
        [scanner scanHexInt:&anInt];
        NSData *entity = [[NSData alloc] initWithBytes:&anInt length:1];
        [hexData appendData:entity];
        
        range.location += range.length;
        range.length = 2;
    }
    return hexData;
}

+ (NSString *)convertDataToHexStr:(NSData *)data {
    if (!data || [data length] == 0) {
        return @"";
    }
    
    NSMutableString *string = [[NSMutableString alloc] initWithCapacity:[data length]];
    [data enumerateByteRangesUsingBlock:^(const void *bytes, NSRange byteRange, BOOL *stop) {
        unsigned char *dataBytes = (unsigned char*)bytes;
        for (NSInteger i = 0; i < byteRange.length; i++) {
            NSString *hexStr = [NSString stringWithFormat:@"%x", (dataBytes[i]) & 0xff];
            if ([hexStr length] == 2) {
                [string appendString:hexStr];
            } else {
                [string appendFormat:@"0%@", hexStr];
            }
        }
    }];
    return string;
}

+ (NSString *)getValueFromUrl:(NSURL *)url forParam:(NSString *)param {
    NSString *query = [url query];
    
    NSRange start = [query rangeOfString:[param stringByAppendingString:@"="]];
    if (start.location == NSNotFound) {
        return nil;
    }
    NSRange end = [[query substringFromIndex:start.location + start.length] rangeOfString:@"&"];
    NSUInteger index = start.location + start.length;
    
    NSString * str = nil;
    if (end.location == NSNotFound) {
        str = [query substringFromIndex:index];
    } else {
        str = [query substringWithRange:NSMakeRange(index, end.location)];
    }
    str = [str stringByReplacingPercentEscapesUsingEncoding:NSUTF8StringEncoding];
    return str;
}

